function buildTrend(window: TimeWindow, points: TrendPoint[]): TrendBucket[] {
  const config = bucketConfigs[window];
  const end = Date.parse(SNAPSHOT_AT);

  if (points.length !== config.length) {
    throw new Error(`Trend point count mismatch for ${window}: expected ${config.length}, got ${points.length}`);
  }

  return points.map(([status, latencyMs, failureCount], index) => {
    const timestamp = new Date(end - (config.length - index - 1) * config.minutes * 60_000).toISOString();

    return {
      timestamp,
      status,
      latencyMs,
      failureCount,
    };
  });
}

function channels(labels: string[] = ["default"], provider: ProviderTag | ProviderTag[] = "OpenAI", overrides: Record<string, Partial<Record<TimeWindow, TrendPoint[]>>> = {}): RelayChannel[] {
  return labels.map((label, index) => ({
    id: `${label.toLowerCase().replace(/[^a-z0-9]+/g, "-") || "default"}-${index}`,
    provider: Array.isArray(provider) ? provider[index] ?? provider[0] : provider,
    label,
    trends: trendSet(overrides[label]),
  }));
}

function trendSet(points: Partial<Record<TimeWindow, TrendPoint[]>> = {}): RelayChannel["trends"] {
  return {
    "90m": buildTrend("90m", points["90m"] ?? stable90m),
    "24h": buildTrend("24h", points["24h"] ?? stable24h),
    "7d": buildTrend("7d", points["7d"] ?? stable7d),
    "30d": buildTrend("30d", points["30d"] ?? stable30d),
  };
}

function withLeadingNoData(points: TrendPoint[], count: number): TrendPoint[] {
  return points.map((point, index) => (index < count ? ["no_data", null, 0] : point));
}

